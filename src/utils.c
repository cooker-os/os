void sleep(double counter) {
    counter=counter*1000000;
    for (double i=0; i<counter; i++);
}

void backspace(int num) 
{
    int five = num/5;
    int mod = num%5;
    for (int i=0; i<five; i++)
        printk("\b\b\b\b\b");

    for (int i=0; i<mod; i++)
        printk("\b");
}

